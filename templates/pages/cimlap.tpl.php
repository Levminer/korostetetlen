    Mitől különleges a pizzériánk?
</h2>

<img src="./images/pizza.jpg" alt="Árpádhalom" class="img-responsive">

<p>
    A Nonstop SMS Pizzéria éjjel-nappal várja a pizzarajongókat. Egyszerű, gyors és kényelmes csak küldj egy üzenetet! Minden SMS egyetlen pizzára vonatkozik, így a rendelés mindig átlátható.

Folyamatos nyitvatartásunknak köszönhetően bármikor csillapíthatod az éhséget. Akár egy hosszú műszak után, akár egy buli közben, ránk számíthatsz. Kínálatunkban klasszikus és különleges pizzák is megtalálhatók.

A rendelésedet perceken belül visszaigazoljuk SMS-ben. A Nonstop SMS Pizzéria ahol a nap 24 órájában csak egy üzenet választ el a kedvenc pizzádtól.
</p>