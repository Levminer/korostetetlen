<h2>
    Mitől különleges a pizzériánk?
</h2>

<img src="./images/pizza.jpg" alt="Árpádhalom" class="img-responsive cimlap-kep">

<p>
    A Nonstop SMS Pizzéria éjjel-nappal várja a pizzarajongókat. Egyszerű, gyors és kényelmes a rendelés: csak küldj egy üzenetet. Minden SMS egyetlen pizzára vonatkozik, így a rendelés mindig átlátható.
</p>

<p>
    Folyamatos nyitvatartásunknak köszönhetően bármikor csillapíthatod az éhséget. Akár egy hosszú műszak után, akár egy buli közben, ránk számíthatsz. Kínálatunkban klasszikus és különleges pizzák is megtalálhatók.
</p>

<p>
    A rendelésedet perceken belül visszaigazoljuk SMS-ben, hogy pontosan tudd, mikor indul hozzád a kedvenc pizza. Célunk, hogy gyors, megbízható és finom megoldást adjunk minden napszakban.
</p>

<p>
    A Nonstop SMS Pizzéria, ahol a nap 24 órájában csak egy üzenet választ el a frissen sült pizzádtól.
</p>